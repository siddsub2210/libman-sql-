    private void displayRecords() {
        try {
            // SQL query
            String sql ="select memberID, name, phone, email, count_late_returns(memberID) AS count1 FROM member where count_late_returns(memberID) > 3 ";
            ps = con.prepareStatement(sql);
            
            // Create statement and execute query
            rs = ps.executeQuery();

            // Create table model to hold data from result set
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0); // Clear existing rows

            // Populate table model with data from result set
            while (rs.next()) {
                Object[] row = {
                    rs.getString("memberID"),
                    rs.getString("name"),
                    rs.getString("phone"),
                    rs.getString("email"),
                    rs.getString("count1")
                    // Add more columns as needed
                };
                tableModel.addRow(row);
            }

        } catch (SQLException e) {
            Logger.getLogger(ViewBooksBorrowed.class.getName()).log(Level.SEVERE, null, this);
            JOptionPane.showMessageDialog(this, "Error retrieving records from database.");
        }
    }
    
    
    
